module gen_wr_data (
	input			clk		,
	//	input			reset	,
	output	[31:0]	ov_image_data

	);

	//	ref signals

	reg		[7:0]	lfsr 	= 8'hab;

	//	ref ARCHITECTURE


	always @ (posedge clk) begin
		lfsr[0]	<= lfsr[7];
		lfsr[1]	<= lfsr[6];
		lfsr[2]	<= lfsr[5]^lfsr[4];
		lfsr[3]	<= lfsr[4]^lfsr[3];
		lfsr[4]	<= lfsr[3]^lfsr[2];
		lfsr[5]	<= lfsr[2]^lfsr[1];
		lfsr[6]	<= lfsr[1];
		lfsr[7]	<= lfsr[0];
	end

	assign	ov_image_data	= {4{lfsr}};

endmodule

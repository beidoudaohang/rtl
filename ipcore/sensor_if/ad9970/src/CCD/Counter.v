//仿真单位/精度
`timescale 1ns/1ps
//-------------------------------------------------------------------------------------------------

`timescale 1 ns / 1 ps
module counter (
	input 	 			clk		,		//时钟
	input				hend	,
	input 	 			i_clk_en,		//时钟使能
	input 	 			i_aclr	,		//同步复位
	output reg [15:0]	ov_q			//计数输出
	);

	always@(posedge clk) begin
		if(hend) begin
			if(i_aclr) begin
				ov_q <= 16'h0;
			end else if(i_clk_en) begin
				ov_q <= ov_q + 16'h1;
			end else begin
				ov_q <= ov_q;
			end
		end

	end

endmodule

module testcase_2 ();

	//	ref signals
	//	===============================================================================================
	//	--ref parameter
	//	===============================================================================================
	//	-------------------------------------------------------------------------------------
	//	TESTCASE
	//	-------------------------------------------------------------------------------------
	parameter	TESTCASE_NUM			= "testcase_2"			;	//其他模块需要使用字符串
	//	-------------------------------------------------------------------------------------
	//	dut paramter
	//	-------------------------------------------------------------------------------------
	parameter	FIFO_WIDTH		= 8		;
	parameter	FIFO_DEPTH		= 16	;

	//	-------------------------------------------------------------------------------------
	//	monitor paramter
	//	-------------------------------------------------------------------------------------
	parameter	MONITOR_OUTPUT_FILE_EN			= 0						;	//是否产生输出文件
	parameter	MONITOR_OUTPUT_FILE_PATH		= "file/sync_buffer_file/"	;	//产生的数据要写入的路径
	parameter	CHK_INOUT_DATA_STOP_ON_ERROR	= 0						;
	parameter	CHK_PULSE_WIDTH_STOP_ON_ERROR	= 0						;

	//	-------------------------------------------------------------------------------------
	//	testcase parameter
	//	-------------------------------------------------------------------------------------
	parameter	CLK_PERIOD_WR				= 15	;	//时钟频率
	parameter	CLK_PERIOD_RD				= 23	;	//时钟频率

	//	===============================================================================================
	//	--ref signal
	//	===============================================================================================
	//	-------------------------------------------------------------------------------------
	//	dut signal
	//	-------------------------------------------------------------------------------------
	reg								reset_async		= 1'b0	;
	reg								clk_wr			= 1'b0	;
	reg								i_wr_en			= 1'b0	;
	reg		[FIFO_WIDTH-1:0]		iv_fifo_din		= 'b0	;
	reg								clk_rd			= 1'b0	;
//	wire							i_rd_en			;
	reg								i_rd_en			= 1'b0	;

	//	-------------------------------------------------------------------------------------
	//	testbench
	//	-------------------------------------------------------------------------------------
	reg								rd_begin		= 1'b0;

	//	ref ARCHITECTURE

	//	===============================================================================================
	//	ref ***tb 子模块激励***
	//	===============================================================================================
	//	-------------------------------------------------------------------------------------
	//	--ref DUT
	//	-------------------------------------------------------------------------------------
	//	-------------------------------------------------------------------------------------
	//	时钟复位
	//	-------------------------------------------------------------------------------------
	always	#(CLK_PERIOD_WR/2.0)	clk_wr	= !clk_wr;
	always	#(CLK_PERIOD_RD/2.0)	clk_rd	= !clk_rd;

	initial begin
		reset_async	= 1'b1;
		#200;
		reset_async	= 1'b0;
	end

	//	-------------------------------------------------------------------------------------
	//	写
	//	-------------------------------------------------------------------------------------
	//	always @ (posedge clk_wr) begin
	//		i_wr_en	<= $random();
	//	end

	initial begin
		forever begin
			wait(harness.o_fifo_full==1'b0 && harness.o_fifo_empty==1'b1);
			@(posedge clk_wr);
			i_wr_en	= 1'b1;
			wait(harness.o_fifo_full==1'b1);
			@(posedge clk_wr);
			i_wr_en	= 1'b0;
		end
	end

	always @ (posedge clk_wr) begin
		if((i_wr_en==1)&&(harness.o_fifo_full==1'b0)) begin
			iv_fifo_din	<= iv_fifo_din + 1'b1;
		end
	end

	//	-------------------------------------------------------------------------------------
	//	读
	//	-------------------------------------------------------------------------------------
	initial begin
		forever begin
			wait(harness.o_fifo_full==1'b1 && harness.o_fifo_empty==1'b0);
			@(posedge clk_rd);
			i_rd_en	= 1'b1;
			wait(harness.o_fifo_empty==1'b1);
			@(posedge clk_rd);
			i_rd_en	= 1'b0;
		end
	end

	//	-------------------------------------------------------------------------------------
	//	--ref 仿真时间
	//	-------------------------------------------------------------------------------------
	initial begin
		#200
		#10000
		#200
		$stop;
	end


endmodule
